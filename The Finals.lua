-- ??????????? ??????? ?? { key = N } ? ?????? { keyN1, keyN2 ... keyNx }
function convertN(tbl)
	local result = {}
	for k, n in pairs(tbl) do
		for _=1,n,1 do
			table.insert(result, k)
		end
	end
	return result
end

